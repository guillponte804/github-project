def add_numbers(a, b):
    """
    Add two numbers and return the result.
    
    Args:
    a: A float or integer representing the first number.
    b: A float or integer representing the second number.
    
    Returns:
    The sum of a and b.
    """
    return a + b

# Example usage:
result = add_numbers(5, 3)
print(result)  # Output: 8
